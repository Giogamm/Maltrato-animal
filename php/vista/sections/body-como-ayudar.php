        <h2>¿Como podemos ayudar?</h2>
        <p>El maltrato animal es un problema que afecta a millones de animales en todo el mundo. Aunque no podemos
            salvar a
            todos los animales, hay muchas cosas que podemos hacer para ayudar a combatir el maltrato animal y
            contribuir a
            crear un mundo mejor para ellos. </p>

        <p>Según datos del Instituto Nacional de Estadística y Geografía (INEGI), en México hay una preocupante cantidad
            de
            mascotas en situación de calle con aproximadamente un 70% del total de su población. De acuerdo con la
            Procuraduría
            Ambiental y del Ordenamiento Territorial (PAOT), el abandono de mascotas tiene un promedio de crecimiento
            del 20% anual,
            aunque con reportes de diversos refugios y centros de protección animal, durante el periodo de cuarentena el
            número de
            perros y gatos que las personas llevan a sus instalaciones ha incrementado con respecto a otros años.</p>

        <p>Otro de los aspectos que más influyen en el abandono es la venta ilegal y descontrolada de animales, donde
            muchas veces
            si no se venden cuando son cachorros los animales son dejados a su suerte, o los compradores abandonan a sus
            mascotas
            cuando se dan cuenta que no son de “raza pura”.</p>
        <img src="../../img/img-formas-de-ayudar.webp" alt="" class="img-formas-de-ayudar">
        <h3>1. Adopción definitiva o temporal de mascotas</h3>
        <p>Una de las formas más efectivas de ayudar a los animales es adoptar una mascota. Al adoptar un animal, no
            solo
            le das una segunda oportunidad en la vida, sino que también ayudas a reducir la sobrepoblación de animales
            en
            los refugios y a combatir el maltrato animal.</p>
        <h3>2. Denunciar el maltrato animal</h3>
        <p>Si ves a un animal siendo maltratado, no dudes en denunciarlo. Puedes llamar a la policía, a la protectora de
            animales o a cualquier otra autoridad competente para reportar el maltrato animal. Recuerda que los animales
            no pueden hablar por sí mismos, por lo que dependen de nosotros para protegerlos.</p>
        <h3>3. Ser voluntario en una protectora de animales</h3>
        <p>Si no puedes adoptar una mascota, puedes ser voluntario en una protectora de animales. Las protectoras
            siempre
            necesitan ayuda para cuidar a los animales, limpiar las jaulas, pasear a los perros, etc. Ser voluntario en
            una
            protectora de animales es una forma muy gratificante de ayudar a los animales y de contribuir a crear un
            mundo
            mejor para ellos.</p>
        <h3>4. Educar a los demás sobre el maltrato animal</h3>
        <p>Otra forma de ayudar a los animales es educar a los demás sobre el maltrato animal. Muchas personas no son
            conscientes de la crueldad que sufren los animales en todo el mundo, por lo que es importante concienciar a
            la
            sociedad sobre este problema. Puedes compartir información sobre el maltrato animal en tus redes sociales,
            hablar con tus amigos y familiares sobre el tema, etc.</p>
        <h3>5. Apoyar a organizaciones que luchan contra el maltrato animal</h3>
        <p>Hay muchas organizaciones que luchan contra el maltrato animal y que necesitan apoyo para seguir adelante con
            su
            labor. Puedes hacer donaciones, participar en eventos benéficos, comprar productos solidarios, etc. para
            ayudar a estas organizaciones a seguir protegiendo a los animales.</p>
